def _count_quantifiers(expr: Expr) -> int:
    """Count the total number of bound variables across all quantifiers."""
    if isinstance(expr, (syntax.Bool, syntax.Int, Id, AppExpr)):
        if isinstance(expr, AppExpr):
            return sum(_count_quantifiers(a) for a in expr.args)
        return 0
    elif isinstance(expr, syntax.UnaryExpr):
        return _count_quantifiers(expr.arg)
    elif isinstance(expr, syntax.BinaryExpr):
        return _count_quantifiers(expr.arg1) + _count_quantifiers(expr.arg2)
    elif isinstance(expr, NaryExpr):
        return sum(_count_quantifiers(a) for a in expr.args)
    elif isinstance(expr, syntax.QuantifierExpr):
        return len(expr.binder.vs) + _count_quantifiers(expr.body)
    elif isinstance(expr, syntax.IfThenElse):
        return (_count_quantifiers(expr.branch) + _count_quantifiers(expr.then)
                + _count_quantifiers(expr.els))
    elif isinstance(expr, syntax.Let):
        return _count_quantifiers(expr.val) + _count_quantifiers(expr.body)
    else:
        assert False, (type(expr), expr)


def _count_alternations(expr: Expr, cur_quant: Optional[str] = None, alts: int = 0, polarity: int = 1) -> int:
    """
    Count nested quantifier alternations, respecting polarity.
    Negation (and the antecedent of an implication) flips the effective quantifier type:
      forall X. !(forall Y. ...)  ==  forall X. exists Y. ...  => 1 alternation
      forall X. !(exists Y. ...)  ==  forall X. forall Y. ...  => 0 alternations
    Tracks the maximum alternations along any root-to-leaf path.
    """
    if isinstance(expr, syntax.QuantifierExpr):
        effective = expr.quant if polarity > 0 else ('EXISTS' if expr.quant == 'FORALL' else 'FORALL')
        new_alts = alts + (1 if cur_quant is not None and effective != cur_quant else 0)
        return _count_alternations(expr.body, effective, new_alts, polarity)
    elif isinstance(expr, (syntax.Bool, syntax.Int, Id)):
        return alts
    elif isinstance(expr, AppExpr):
        if not expr.args:
            return alts
        return max(_count_alternations(a, cur_quant, alts, polarity) for a in expr.args)
    elif isinstance(expr, syntax.UnaryExpr):
        new_pol = -polarity if expr.op == 'NOT' else polarity
        return _count_alternations(expr.arg, cur_quant, alts, new_pol)
    elif isinstance(expr, syntax.BinaryExpr):
        if expr.op == 'IMPLIES':
            return max(
                _count_alternations(expr.arg1, cur_quant, alts, -polarity),  # antecedent: flipped
                _count_alternations(expr.arg2, cur_quant, alts, polarity),
            )
        elif expr.op == 'IFF':
            # A <-> B appears in both polarities; take max over both
            return max(
                _count_alternations(expr.arg1, cur_quant, alts, polarity),
                _count_alternations(expr.arg1, cur_quant, alts, -polarity),
                _count_alternations(expr.arg2, cur_quant, alts, polarity),
                _count_alternations(expr.arg2, cur_quant, alts, -polarity),
            )
        else:
            return max(
                _count_alternations(expr.arg1, cur_quant, alts, polarity),
                _count_alternations(expr.arg2, cur_quant, alts, polarity),
            )
    elif isinstance(expr, NaryExpr):
        if not expr.args:
            return alts
        return max(_count_alternations(a, cur_quant, alts, polarity) for a in expr.args)
    elif isinstance(expr, syntax.IfThenElse):
        return max(
            _count_alternations(expr.branch, cur_quant, alts, polarity),
            _count_alternations(expr.then, cur_quant, alts, polarity),
            _count_alternations(expr.els, cur_quant, alts, polarity),
        )
    elif isinstance(expr, syntax.Let):
        return max(
            _count_alternations(expr.val, cur_quant, alts, polarity),
            _count_alternations(expr.body, cur_quant, alts, polarity),
        )
    else:
        assert False, (type(expr), expr)


def _count_boolean_connectives(expr: Expr) -> int:
    """Count binary boolean connectives: AND, OR, IMPLIES, IFF (negation excluded)."""
    if isinstance(expr, (syntax.Bool, syntax.Int, Id)):
        return 0
    elif isinstance(expr, AppExpr):
        return sum(_count_boolean_connectives(a) for a in expr.args)
    elif isinstance(expr, syntax.UnaryExpr):
        return _count_boolean_connectives(expr.arg)
    elif isinstance(expr, syntax.BinaryExpr):
        is_bool_op = expr.op in ('IMPLIES', 'IFF')
        return (1 if is_bool_op else 0) + _count_boolean_connectives(expr.arg1) + _count_boolean_connectives(expr.arg2)
    elif isinstance(expr, NaryExpr):
        is_bool_op = expr.op in ('AND', 'OR')
        # NaryExpr with n args contributes (n-1) binary connectives
        count = (len(expr.args) - 1) if is_bool_op else 0
        return count + sum(_count_boolean_connectives(a) for a in expr.args)
    elif isinstance(expr, syntax.QuantifierExpr):
        return _count_boolean_connectives(expr.body)
    elif isinstance(expr, syntax.IfThenElse):
        return (_count_boolean_connectives(expr.branch) + _count_boolean_connectives(expr.then)
                + _count_boolean_connectives(expr.els))
    elif isinstance(expr, syntax.Let):
        return _count_boolean_connectives(expr.val) + _count_boolean_connectives(expr.body)
    else:
        assert False, (type(expr), expr)


def _is_atom(expr: Expr) -> bool:
    """An atomic formula: relation application, equality/comparison, Bool, or Id (nullary relation)."""
    if isinstance(expr, (syntax.Bool, Id)):
        return True
    if isinstance(expr, AppExpr):
        return True
    if isinstance(expr, syntax.BinaryExpr) and expr.op in ('EQUAL', 'NOTEQ', 'GE', 'GT', 'LE', 'LT'):
        return True
    return False


def _is_literal(expr: Expr) -> bool:
    """An atom or its negation."""
    if isinstance(expr, syntax.UnaryExpr) and expr.op == 'NOT':
        return _is_atom(expr.arg)
    return _is_atom(expr)


def _is_cube(expr: Expr) -> bool:
    """
    True if expr has the structure of a cube, with quantifiers allowed anywhere.
    At each level, strips any leading quantifiers, then checks:
      - a literal
      - AND whose conjuncts are each cubes
      - NOT(clause)
    """
    while isinstance(expr, syntax.QuantifierExpr):
        expr = expr.body
    if _is_literal(expr):
        return True
    if isinstance(expr, NaryExpr) and expr.op == 'AND':
        return all(_is_cube(a) for a in expr.args)
    if isinstance(expr, syntax.UnaryExpr) and expr.op == 'NOT':
        return _is_clause(expr.arg)
    return False


def _is_clause(expr: Expr) -> bool:
    """
    True if expr has the structure of a clause, with quantifiers allowed anywhere.
    At each level, strips any leading quantifiers, then checks:
      - a literal
      - OR whose disjuncts are each clauses
      - NOT(cube)
      - cube -> clause
    """
    while isinstance(expr, syntax.QuantifierExpr):
        expr = expr.body
    if _is_literal(expr):
        return True
    if isinstance(expr, NaryExpr) and expr.op == 'OR':
        return all(_is_clause(a) for a in expr.args)
    if isinstance(expr, syntax.BinaryExpr) and expr.op == 'IMPLIES':
        return _is_cube(expr.arg1) and _is_clause(expr.arg2)
    if isinstance(expr, syntax.UnaryExpr) and expr.op == 'NOT':
        return _is_cube(expr.arg)
    return False


def _is_clausal(expr: Expr) -> bool:
    """
    True if expr has the structure of a conjunction of clauses.
    Strips outer FORALL quantifiers and outer AND conjunctions (recursively),
    then delegates to _is_clause.
    """
    if isinstance(expr, syntax.QuantifierExpr) and expr.quant == 'FORALL':
        return _is_clausal(expr.body)
    if isinstance(expr, NaryExpr) and expr.op == 'AND':
        return all(_is_clausal(a) for a in expr.args)
    return _is_clause(expr)


def _max_complexity(exprs: List[Expr]) -> Tuple[int, int, int, bool]:
    """
    Return (max_quantifiers, max_alternations, max_bool_connectives, all_clausal)
    across a non-empty list of expressions.
    """
    assert exprs
    max_q  = max(_count_quantifiers(e) for e in exprs)
    max_a  = max(_count_alternations(e) for e in exprs)
    max_nb = max(_count_boolean_connectives(e) for e in exprs)
    all_cl = all(_is_clausal(e) for e in exprs)
    return max_q, max_a, max_nb, all_cl


def _step_user_exprs(step: syntax.FbiiStepDecl) -> List[Expr]:
    """
    Collect all user-written expressions for a step: body invariants plus
    prophecy-block formulas as written by the user (InvariantDecl.expr, or
    the argument of a proph_select heuristic).
    """
    exprs: List[Expr] = [inv.expr for inv in step.body]
    if step.prophecy is not None:
        for item in step.prophecy:
            if isinstance(item, InvariantDecl):
                exprs.append(item.expr)
            elif isinstance(item, syntax.FbiiHeuristicDecl) and item.arg is not None:
                # proph_select theta — include the theta expression
                exprs.append(item.arg)
    return exprs


def print_fbii_predicate_complexity(prog: syntax.Program) -> None:
    """Print a single complexity table: one row for invariants, one row per FBII step."""
    # --- Collect rows ---
    # Each row: (label, max_q, max_a, max_nb, all_clausal, proph_params_str)
    Row = Tuple[str, int, int, int, bool, str]
    rows: List[Row] = []

    # Invariants row (non-safety)
    inv_exprs: List[Expr] = [inv.expr for inv in prog.invs() if not inv.is_safety]
    if inv_exprs:
        max_q, max_a, max_nb, all_cl = _max_complexity(inv_exprs)
        rows.append(('proof by invariant', max_q, max_a, max_nb, all_cl, '-'))

    # FBII step rows
    fbii_decls = list(prog.fbii_decls())
    for fbii_idx, fbii in enumerate(fbii_decls):
        prefix = f'fbii block {fbii_idx + 1},' if len(fbii_decls) > 1 else 'fbii'
        num_steps = len(fbii.steps)
        for step_idx, step in enumerate(fbii.steps):
            dir_str = step.direction + (' prophecy' if step.has_prophecy else '')
            direction = dir_str + ((f' [{step.name}]') if step.name else '')
            label = f'{prefix} {step_idx + 1}/{num_steps} ({direction})'
            exprs = _step_user_exprs(step)
            if exprs:
                max_q, max_a, max_nb, all_cl = _max_complexity(exprs)
            else:
                max_q, max_a, max_nb, all_cl = 0, 0, 0, True
            pp = str(len(step.params)) if step.has_prophecy else '-'
            rows.append((label, max_q, max_a, max_nb, all_cl, pp))

    if not rows:
        utils.logger.always_print('(no invariants or fbii blocks found)')
        return

    # --- Print table ---
    lbl_w = max(len(r[0]) for r in rows)
    sep = (f'  {"-" * lbl_w}  {"-" * 11}  {"-" * 12}'
           f'  {"-" * 9}  {"-" * 7}  {"-" * 12}')
    utils.logger.always_print(
        f'  {"":<{lbl_w}}  {"quantifiers":>11}  {"alternations":>12}'
        f'  {"bool-conn":>9}  {"clausal":>7}  {"proph-params":>12}'
    )
    utils.logger.always_print(sep)
    for label, max_q, max_a, max_nb, all_cl, pp in rows:
        utils.logger.always_print(
            f'  {label:<{lbl_w}}  {max_q:>11}  {max_a:>12}'
            f'  {max_nb:>9}  {"yes" if all_cl else "no":>7}  {pp:>12}'
        )



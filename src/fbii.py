class _FbiiFailure(Exception):
    """Raised on the first failing FBII check to terminate the proof immediately."""
    pass


def _check(
        s: Solver,
        num_states: int,
        hyps: List[Expr],
        neg_conc: Expr,
        label: str,
        verbose_lines: Optional[List[str]] = None,
        *,
        _silent: bool = False,
) -> None:
    """
    Check validity of (And(*hyps) => conc) by checking unsat of (And(*hyps) & !conc).
    Raises _FbiiFailure if the check fails (sat).
    If verbose_lines is provided and --verbose is set, those lines are printed after the label.
    If _silent is True, suppresses the per-check header and ok. line; on failure prints
    'no! (at: {label})' with the counterexample. Used for group summaries.
    """
    t = s.get_translator(num_states)
    if not _silent:
        utils.logger.always_print(f'  checking {label}')
        if verbose_lines and utils.args.verbose:
            for line in verbose_lines:
                utils.logger.always_print(f'    {line}')
    with s.new_frame():
        for h in hyps:
            s.add(t.translate_expr(h))
        s.add(t.translate_expr(neg_conc))
        m = check_unsat([], s, num_states, verbose=not _silent)
        if m is not None:
            if _silent:
                utils.logger.always_print(f'no! (at: {label})')
                if utils.args.print_counterexample:
                    utils.logger.always_print(f'\ncounterexample:\n{utils.indent(str(m))}')
            raise _FbiiFailure(label)


def _check_initiation_batch(
        s: Solver,
        hyps: List[Expr],
        conjuncts: List[Expr],
        conj_name: str,
        title: str,
        formula: str,
        verbose: bool,
) -> None:
    """Check ``hyps => ci`` for each ci.

    Hypotheses are pushed once in an outer frame so Z3 can reuse learned
    clauses about them across all per-conjunct queries.
    """
    t = s.get_translator(1)
    n = len(conjuncts)
    utils.logger.always_print(f'  checking {title} ({formula})')
    _t = datetime.now()
    with s.new_frame():
        for h in hyps:
            s.add(t.translate_expr(h))
        for i, ci in enumerate(conjuncts):
            label = f'{title} (conjunct {i + 1}/{n}): {formula}'
            if verbose:
                utils.logger.always_print(f'    {conj_name}[{i + 1}/{n}]: {ci}... ', end='')
            _t_ci = datetime.now()
            with s.new_frame():
                s.add(t.translate_expr(Not(ci)))
                m = check_unsat([], s, 1, verbose=False)
                if m is not None:
                    if verbose:
                        utils.logger.always_print(f'no!')
                    else:
                        utils.logger.always_print(f'no! (at: {label})')
                    if utils.args.print_counterexample:
                        utils.logger.always_print(f'\ncounterexample:\n{utils.indent(str(m))}')
                    raise _FbiiFailure(label)
            if verbose:
                utils.logger.always_print(f'ok. ({datetime.now() - _t_ci})')
    if verbose:
        utils.logger.always_print(f'  ok. ({datetime.now() - _t})')
    else:
        utils.logger.always_print(f'ok. ({datetime.now() - _t})')


def _check_consecution_batch(
        s: Solver,
        prog: syntax.Program,
        base_hyps: List[Expr],
        conjuncts: List[Expr],
        prime_conc: bool,
        conj_name: str,
        title: str,
        formula: str,
        verbose: bool,
        extra_mods: Tuple[syntax.ModifiesClause, ...] = (),
) -> None:
    """Check consecution across all transitions.

    Uses three nested frames mirroring ``verify``'s structure:
      outer  — base hypotheses (pushed once, shared across all transitions)
      middle — transition formula (pushed once per transition, shared across conjuncts)
      inner  — negated conclusion (pushed once per conjunct)
    """
    t = s.get_translator(2)
    n = len(conjuncts)
    if not verbose:
        utils.logger.always_print(f'  checking {title} ({formula})')
    _t = datetime.now()
    with s.new_frame():
        for h in base_hyps:
            s.add(t.translate_expr(h))
        for ition in prog.transitions():
            tr_expr = ition.as_twostate_formula(prog.scope, extra_mods=extra_mods)
            if verbose:
                utils.logger.always_print(f'  checking {title} (transition {ition.name}): {formula}')
            _t_tr = datetime.now()
            with s.new_frame():
                s.add(t.translate_expr(tr_expr))
                for i, ci in enumerate(conjuncts):
                    label = f'{title} (transition {ition.name}, conjunct {i + 1}/{n}): {formula}'
                    neg_conc = Not(New(ci)) if prime_conc else Not(ci)
                    if verbose:
                        utils.logger.always_print(f'    {conj_name}[{i + 1}/{n}]: {ci}... ', end='')
                    _t_ci = datetime.now()
                    with s.new_frame():
                        s.add(t.translate_expr(neg_conc))
                        m = check_unsat([], s, 2, verbose=False)
                        if m is not None:
                            if verbose:
                                utils.logger.always_print(f'no!')
                            else:
                                utils.logger.always_print(f'no! (at: {label})')
                            if utils.args.print_counterexample:
                                utils.logger.always_print(f'\ncounterexample:\n{utils.indent(str(m))}')
                            raise _FbiiFailure(label)
                    if verbose:
                        utils.logger.always_print(f'ok. ({datetime.now() - _t_ci})')
            if verbose:
                utils.logger.always_print(f'  ok. ({datetime.now() - _t_tr})')
    if not verbose:
        utils.logger.always_print(f'ok. ({datetime.now() - _t})')



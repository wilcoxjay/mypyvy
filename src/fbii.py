def split_conjuncts(expr: Expr) -> List[Expr]:
    """Recursively flatten nested AND nodes into a list of conjuncts."""
    if isinstance(expr, NaryExpr) and expr.op == 'AND':
        result: List[Expr] = []
        for arg in expr.args:
            result.extend(split_conjuncts(arg))
        return result
    return [expr]


def _subst_inv_p_in_expr(
        expr: Expr,
        params: Tuple[SortedVar, ...],
        body_invs: List[InvariantDecl],
        scope: syntax.Scope,
) -> Expr:
    """
    Walk expr replacing every __INV_P(args...) application with the conjunction
    of body invariants with params substituted by args.
    Also replace nullary __INV_P (Id node) when there are no params.
    """
    if isinstance(expr, syntax.Bool) or isinstance(expr, syntax.Int):
        return expr
    elif isinstance(expr, syntax.UnaryExpr):
        return syntax.UnaryExpr(expr.op, _subst_inv_p_in_expr(expr.arg, params, body_invs, scope), span=expr.span)
    elif isinstance(expr, syntax.BinaryExpr):
        return syntax.BinaryExpr(
            expr.op,
            _subst_inv_p_in_expr(expr.arg1, params, body_invs, scope),
            _subst_inv_p_in_expr(expr.arg2, params, body_invs, scope),
            span=expr.span,
        )
    elif isinstance(expr, NaryExpr):
        return NaryExpr(
            expr.op,
            tuple(_subst_inv_p_in_expr(a, params, body_invs, scope) for a in expr.args),
            span=expr.span,
        )
    elif isinstance(expr, AppExpr):
        if expr.callee == '__INV_P':
            # Replace __INV_P(args) with INV(args), i.e., body conjuncts with P -> args
            assert len(expr.args) == len(params), \
                f'__INV_P applied to {len(expr.args)} args but step has {len(params)} params'
            gamma = {Id(sv.name): arg for sv, arg in zip(params, expr.args)}
            conjs = [syntax.subst(scope, inv.expr, gamma) for inv in body_invs]
            return And(*conjs)
        else:
            return AppExpr(
                expr.callee,
                tuple(_subst_inv_p_in_expr(a, params, body_invs, scope) for a in expr.args),
                n_new=expr.n_new,
                span=expr.span,
            )
    elif isinstance(expr, Id):
        if expr.name == '__INV_P' and len(params) == 0:
            return And(*[inv.expr for inv in body_invs])
        return expr
    elif isinstance(expr, syntax.QuantifierExpr):
        new_body = _subst_inv_p_in_expr(expr.body, params, body_invs, scope)
        return syntax.QuantifierExpr(expr.quant, expr.binder.vs, new_body, span=expr.span)
    elif isinstance(expr, syntax.IfThenElse):
        return syntax.IfThenElse(
            _subst_inv_p_in_expr(expr.branch, params, body_invs, scope),
            _subst_inv_p_in_expr(expr.then, params, body_invs, scope),
            _subst_inv_p_in_expr(expr.els, params, body_invs, scope),
            span=expr.span,
        )
    elif isinstance(expr, syntax.Let):
        return syntax.Let(
            expr.binder.vs[0],
            _subst_inv_p_in_expr(expr.val, params, body_invs, scope),
            _subst_inv_p_in_expr(expr.body, params, body_invs, scope),
            span=expr.span,
        )
    else:
        assert False, (type(expr), expr)


def build_pinv_conjuncts(
        step: FbiiStepDecl,
        scope: syntax.Scope,
) -> List[Expr]:
    """
    Build the list of prophecy conjuncts for a step.

    If the step has a prophecy block: apply __INV_P substitution and split.
    Otherwise: build the default PINV based on direction.
    """
    params = step.params
    body_invs = step.body
    inv_conj = And(*[inv.expr for inv in body_invs])

    if step.prophecy is not None:
        # Substitute __INV_P in each prophecy item, collect all conjuncts
        result: List[Expr] = []
        for item in step.prophecy:
            if isinstance(item, syntax.FbiiHeuristicDecl):
                raw_expr = _expand_heuristic(item, step.direction, params)
            else:
                raw_expr = item.expr
            subst_expr = _subst_inv_p_in_expr(raw_expr, params, body_invs, scope)
            result.extend(split_conjuncts(subst_expr))
        return result if result else [TrueExpr]
    else:
        # Default prophecy invariants:
        mp_app = _mp_app(params)
        if step.direction == 'forward':
            default = Exists(params, And(inv_conj, mp_app))
        else:
            default = Exists(params, And(inv_conj, Not(mp_app)))
        return split_conjuncts(default)


def _mp_app(params: Tuple[SortedVar, ...]) -> Expr:
    if params:
        return AppExpr('__M_P', tuple(Id(p.name) for p in params))
    else:
        return Id('__M_P')


def _inv_p_app(params: Tuple[SortedVar, ...]) -> Expr:
    if params:
        return AppExpr('__INV_P', tuple(Id(p.name) for p in params))
    else:
        return Id('__INV_P')


def _expand_heuristic(
        heuristic: syntax.FbiiHeuristicDecl,
        direction: str,
        params: Tuple[SortedVar, ...],
) -> Expr:
    """Expand a prophecy heuristic to an expression (may contain __INV_P and __M_P)."""
    mp = _mp_app(params)
    inv_p = _inv_p_app(params)
    if heuristic.name == 'proph_default':
        # Default: exists P. __INV_P(P) & __M_P(P)  (forward)
        #          exists P. __INV_P(P) & !__M_P(P) (backward)
        if direction == 'forward':
            return Exists(params, And(inv_p, mp))
        else:
            return Exists(params, And(inv_p, Not(mp)))
    elif heuristic.name == 'proph_select':
        # proph_select theta:
        #   forward:  (!theta -> forall P. __M_P(P))  & (theta -> exists P. __INV_P(P) & __M_P(P))
        #   backward: (!theta -> forall P. !__M_P(P)) & (theta -> exists P. __INV_P(P) & !__M_P(P))
        assert heuristic.arg is not None
        theta = heuristic.arg
        if direction == 'forward':
            return And(
                Implies(Not(theta), Forall(params, mp)),
                Implies(theta, Exists(params, And(inv_p, mp))),
            )
        else:
            return And(
                Implies(Not(theta), Forall(params, Not(mp))),
                Implies(theta, Exists(params, And(inv_p, Not(mp)))),
            )
    else:
        assert False, f'unknown heuristic: {heuristic.name!r}'



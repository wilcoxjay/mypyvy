"""
Forward-backward incremental induction (FBII) proof checker.

FBII is an incremental proof technique for establishing safety of a transition system.
A proof is a sequence of steps, each associating a set of invariants (the step
body) in a given direction (forward or backward). Steps are either simple or prophecy:

  Simple step (no `prophecy` keyword): verified by plain induction (2 SMT checks).
    Forward:  1. INIT & PREV => INV
              2. INV & TR & PREV & PREV' => INV'
    Backward: 1. BAD & PREV => INV
              2. INV' & TR & PREV & PREV' => INV

  Prophecy step (`prophecy` keyword): introduces a fresh mutable relation __M_P that holds on viable prophecy
    choices, to show that for every forward / backward trace there exists a choice of prophecy parameters such
    that the body invariants hold at every state. Verified by three SMT checks:

    Forward step (proves the body invariants hold for some prophecy "from init", if bad is reached):
      1. INIT_PROPH  =>  PROPH_INV        (initiation)
      2. PROPH_INV & TR_PROPH  =>  PROPH_INV'   (consecution, per transition)
      3. PROPH_INV  =>  !BAD_PROPH        (equivalent to BAD => there exists a prophecy satisfying the body invariants)

    Backward step (proves the body invariants hold for some prophecy "from bad", if init is reached):
      1. BAD_PROPH   =>  PROPH_INV        (initiation from bad)
      2. PROPH_INV' & TR_PROPH  =>  PROPH_INV   (consecution, per transition)
      3. PROPH_INV  =>  !INIT_PROPH       (equivalent to INIT => there exists a prophecy satisfying the body invariants)

    Where:
      INIT_PROPH = INIT & PREV & (forall P. __INV_P(P) -> __M_P(P)) [negated: INIT & PREV -> exists P. __INV_P(P) & !__M_P(P)]
      BAD_PROPH  = BAD  & PREV & (forall P. __INV_P(P) -> !__M_P(P)) [negated: BAD & PREV -> exists P. __INV_P(P) & __M_P(P)]
      TR_PROPH   = TR & PREV & PREV' & (forall P. __M_P(P) & __INV_P(P) & __INV_P'(P) -> __M_P'(P))
      PROPH_INV  = the prophecy invariants declared in the step's `by` block

The proof is incremental: PREV is the conjunction of all body invariants accumulated from
previous steps (empty for the first step). Each step's body invariants are added to PREV
after the step is verified, so later steps can rely on earlier ones and use earlier prophecy symbols.

After all steps, a final check verifies that the accumulated invariants establish
safety (BAD exclusion: INVARIANTS => SAFETY) or disjointness from init
(INIT exclusion: INVARIANTS => !INIT); at least one must hold.
This implies that no traces from init to bad exist, and thus the program is safe.

Prophecy invariants can be written explicitly as invariant formulas, or generated by heuristics:
  proph_default       — exists P. __INV_P(P) & __M_P(P)   (forward) / exists P. __INV_P(P) & !__M_P(P) (backward)
  proph_select theta  — encodes a choice: if theta holds, use proph_default; otherwise
                        require the prophecy relation to hold for all/no parameters; explicitly:
    forward:  (!theta -> forall P. __M_P(P))  & (theta -> exists P. __INV_P(P) & __M_P(P))
    backward: (!theta -> forall P. !__M_P(P)) & (theta -> exists P. __INV_P(P) & !__M_P(P))
"""


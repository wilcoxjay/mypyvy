def check_fbii(s: Solver) -> None:
    prog = syntax.the_program

    fbii_decls = list(prog.fbii_decls())
    if not fbii_decls:
        utils.logger.always_print('warning: no fbii blocks found')
        return

    for fbii in fbii_decls:
        try:
            _check_fbii_proof(s, prog, prog.scope, fbii)
        except _FbiiFailure:
            utils.exit(1)
            return
        utils.logger.always_print(
            '\nprogram proven SAFE via forward-backward incremental induction with prophecy'
        )


def _check_step_simple(
        s: Solver,
        prog: syntax.Program,
        direction: str,
        init_expr: Expr,
        bad_expr: Expr,
        prev: Expr,
        inv_conj: Expr,
        verbose: bool,
) -> None:
    """Simple step: plain induction (no prophecy variable needed)."""
    inv_conjuncts = split_conjuncts(inv_conj)
    if direction == 'forward':
        _check_initiation_batch(
            s, hyps=[init_expr, prev], conjuncts=inv_conjuncts, conj_name='INV',
            title='forward invariant initiation', formula='INIT & PREV => INV', verbose=verbose,
        )
        _check_consecution_batch(
            s, prog, base_hyps=[inv_conj, prev, New(prev)], conjuncts=inv_conjuncts,
            prime_conc=True, conj_name='INV',
            title='forward invariant consecution', formula="INV & TR & PREV & PREV' => INV'",
            verbose=verbose,
        )
    else:  # backward
        _check_initiation_batch(
            s, hyps=[bad_expr, prev], conjuncts=inv_conjuncts, conj_name='INV',
            title='backward invariant initiation', formula='BAD & PREV => INV', verbose=verbose,
        )
        _check_consecution_batch(
            s, prog, base_hyps=[New(inv_conj), prev, New(prev)], conjuncts=inv_conjuncts,
            prime_conc=False, conj_name='INV',
            title='backward invariant consecution', formula="INV' & TR & PREV & PREV' => INV",
            verbose=verbose,
        )


def _check_step_prophecy(
        s: Solver,
        prog: syntax.Program,
        scope: syntax.Scope,
        step: FbiiStepDecl,
        init_expr: Expr,
        bad_expr: Expr,
        prev: Expr,
        inv_conj: Expr,
        verbose: bool,
) -> None:
    """Prophecy-based path: build PINV and run 3 checks with __M_P."""
    params = step.params
    pinv_conjuncts = build_pinv_conjuncts(step, scope)
    pinv = And(*pinv_conjuncts)

    mp = _mp_app(params)
    init_proph = And(init_expr, prev, Forall(params, Implies(inv_conj, mp)))
    bad_proph  = And(bad_expr,  prev, Forall(params, Implies(inv_conj, Not(mp))))
    tr_proph   = And(prev, New(prev), Forall(params, Implies(And(mp, inv_conj, New(inv_conj)), New(mp))))

    n = len(pinv_conjuncts)
    verbose_pinv = [f'PROPH_INV[{j + 1}/{n}]: {c}' for j, c in enumerate(pinv_conjuncts)]

    if step.direction == 'forward':
        _check_initiation_batch(
            s, hyps=[init_proph], conjuncts=pinv_conjuncts, conj_name='PROPH_INV',
            title='forward prophecy initiation', formula='INIT_PROPH => PROPH_INV', verbose=verbose,
        )
        _check_consecution_batch(
            s, prog, base_hyps=[pinv, tr_proph], conjuncts=pinv_conjuncts,
            prime_conc=True, conj_name='PROPH_INV',
            title='forward prophecy consecution', formula="PROPH_INV & TR_PROPH => PROPH_INV'",
            verbose=verbose, extra_mods=(ModifiesClause('__M_P'),),
        )
        _check(s, 1, hyps=[pinv], neg_conc=bad_proph,
               label='forward prophecy safety (PROPH_INV => !BAD_PROPH)', verbose_lines=verbose_pinv)
    else:  # backward
        _check_initiation_batch(
            s, hyps=[bad_proph], conjuncts=pinv_conjuncts, conj_name='PROPH_INV',
            title='backward prophecy initiation', formula='BAD_PROPH => PROPH_INV', verbose=verbose,
        )
        _check_consecution_batch(
            s, prog, base_hyps=[New(pinv), tr_proph], conjuncts=pinv_conjuncts,
            prime_conc=False, conj_name='PROPH_INV',
            title='backward prophecy consecution', formula="PROPH_INV' & TR_PROPH => PROPH_INV",
            verbose=verbose, extra_mods=(ModifiesClause('__M_P'),),
        )
        _check(s, 1, hyps=[pinv], neg_conc=init_proph,
               label='backward prophecy safety (PROPH_INV => !INIT_PROPH)', verbose_lines=verbose_pinv)


def _check_fbii_proof(
        s: Solver,
        prog: syntax.Program,
        scope: syntax.Scope,
        fbii: FbiiDecl,
) -> None:
    prev_formulas: List[Expr] = []
    num_steps = len(fbii.steps)

    init_expr = And(*[init.expr for init in prog.inits()])
    safety_expr = And(*[safety_decl.expr for safety_decl in prog.safeties()])
    bad_expr = Not(safety_expr)

    for step_idx, step in enumerate(fbii.steps):
        utils.logger.always_print(
            f'\nchecking fbii step {step_idx + 1}/{num_steps} '
            f'({step.direction}{(" [" + step.name + "]") if step.name else ""})'
        )

        params = step.params
        body_invs = step.body
        inv_conj: Expr = And(*[inv.expr for inv in body_invs])
        prev = And(*prev_formulas)
        verbose = getattr(utils.args, 'verbose', False)

        try:
            if not step.has_prophecy:
                _check_step_simple(s, prog, step.direction, init_expr, bad_expr, prev, inv_conj, verbose)
            else:
                param_sorts = tuple(syntax.safe_cast_sort(sv.sort) for sv in params)
                mp_decl = RelationDecl('__M_P', param_sorts, mutable=True)
                with scope.temp_relations(mp_decl):
                    _check_step_prophecy(s, prog, scope, step, init_expr, bad_expr, prev, inv_conj, verbose)
            utils.logger.always_print(f'  step {step_idx + 1} OK')
        except _FbiiFailure:
            utils.logger.always_print(f'  step {step_idx + 1} FAILED')
            raise

        prev_formulas.extend(inv.expr for inv in body_invs)

        # Promote this step's prophecy params to immutable constants in scope
        # so that subsequent steps can reference them in their formulas.
        for sv in step.params:
            actual_sort = syntax.safe_cast_sort(sv.sort)
            scope.constants[sv.name] = ConstantDecl(sv.name, actual_sort, mutable=False)

    inv_all = And(*prev_formulas)
    if not _run_final_checks(s, init_expr, safety_expr, inv_all, fbii.span):
        raise _FbiiFailure('final checks')


def _run_final_checks(
        s: Solver,
        init_expr: Expr,
        safety_expr: Expr,
        prev: Expr,
        span: Optional[syntax.Span],
) -> bool:
    """
    Run BAD exclusion and INIT exclusion final checks.
    Returns True if at least one passes; prints the error and returns False if both fail.
    """
    utils.logger.always_print('')
    utils.logger.always_print('final step (BAD exclusion or INIT exclusion must hold):')

    utils.logger.always_print('  checking BAD exclusion: INVARIANTS => SAFETY')
    bad_excl_model, bad_excl_elapsed = _silent_final_check(s, prev, safety_expr)
    if bad_excl_model is None:
        utils.logger.always_print(f'  BAD exclusion: ok. ({bad_excl_elapsed})')
        return True

    utils.logger.always_print('  BAD exclusion: no!')

    utils.logger.always_print('  checking INIT exclusion: INVARIANTS => !INIT')
    init_excl_model, init_excl_elapsed = _silent_final_check(s, prev, Not(init_expr))
    if init_excl_model is None:
        utils.logger.always_print(f'  INIT exclusion: ok. ({init_excl_elapsed})')
        return True

    utils.logger.always_print('  INIT exclusion: no!')
    if utils.args.print_counterexample:
        utils.logger.always_print('\ncounterexample for BAD exclusion:')
        utils.logger.always_print(utils.indent(str(bad_excl_model)))
        utils.logger.always_print('\ncounterexample for INIT exclusion:')
        utils.logger.always_print(utils.indent(str(init_excl_model)))
    utils.print_error(span, 'fbii proof: final step does not establish safety or disjointness from init')
    return False


def _silent_final_check(s: Solver, prev: Expr, conc: Expr) -> Tuple[Optional[Trace], timedelta]:
    """
    Silently check PREV => CONC (i.e., unsat of PREV & !CONC).
    Returns (model, elapsed_time). model is None if the check passes (unsat).
    """
    t = s.get_translator(1)
    with s.new_frame():
        s.add(t.translate_expr(And(prev, Not(conc))))
        start = datetime.now()
        m = check_unsat([], s, 1, verbose=False)
        elapsed = datetime.now() - start
    return m, elapsed
